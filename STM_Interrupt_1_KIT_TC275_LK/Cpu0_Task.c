void CallBack_Stm0Task1000ms(void)
{
    IfxPort_setPinState(&MODULE_P00, 5, IfxPort_State_toggled);
}

void startTask_Cpu0(void){
    Stm0_StartTimer(0);
}

/* Function
/*********************************************************************************************************************/
/*------------------------------------External Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/
// ---------- Task Definition Function ----------
void initTask_Cpu0(void)
{
    initStm0_Scheduler();

    /* Initialize Task Peripherals */
    IfxPort_setPinMode(&MODULE_P00, 5, IfxPort_Mode_outputPushPullGeneral);    /* Initialize LED port pin                      */
    IfxPort_setPinState(&MODULE_P00, 5, IfxPort_State_high);

    /* Start Task Timer */
    Stm0_NewTimer(CallBack_Stm0Task1000ms,0,500);



}

